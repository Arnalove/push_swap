#include "libft.h"

int	ft_isdigit(char *s)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			if ((s[i] < '0' || s[i] > '9') && (s[i] < 9 || s[i] > 13)
				&& s[i] != ' ' && s[i] != '-' && s[i] != '+')
			{
				return (1);
			}
			if (s[i] == '-' || s[i] == '+')
			{
				if (!s[i + 1])
					return (1);
				else if ((s[i - 1] && (s[i - 1] >= '0' || s[i - 1] <= '9'))
					&& (s[i + 1] && (s[i + 1] >= '0' || s[i + 1] <= '9')))
					return (1);
			}
			i++;
		}
	}
	return (0);
}
